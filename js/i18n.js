const LANG_NAMES = {
  en: 'English', bn: 'বাংলা', hi: 'हिंदी', es: 'Español', ar: 'العربية', de: 'Deutsch'
};

function t(key) {
  const lang = (appState && appState.language) || 'en';
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  return dict[key] || TRANSLATIONS.en[key] || key;
}

function applyLanguage() {
  const lang = (appState && appState.language) || 'en';
  const isRTL = lang === 'ar';
  document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val) el.textContent = val;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = t(key);
    if (val) el.placeholder = val;
  });

  // Update lang display in profile
  const langDisplay = document.getElementById('profile-lang-display');
  if (langDisplay) langDisplay.textContent = LANG_NAMES[lang] || lang;

  // Update active lang option
  document.querySelectorAll('.lang-option').forEach(btn => {
    const btnLang = btn.getAttribute('onclick').match(/'(\w+)'/);
    if (btnLang) {
      btn.classList.toggle('active', btnLang[1] === lang);
    }
  });
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  appState.language = lang;
  storage.save(appState);
  applyLanguage();
  // Re-render visible page
  const activePage = document.querySelector('.page:not(.hidden)');
  if (activePage) {
    const id = activePage.id;
    if (id === 'home-page') renderHome();
    if (id === 'earn-page') renderEarn();
    if (id === 'profile-page') renderProfile();
  }
  closeModal('lang-modal');
}

function selectLanguage(lang) {
  setLanguage(lang);
}
